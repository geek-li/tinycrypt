#ifndef BELT_H
#define BELT_H

#include <stddef.h>
#include <stdint.h>

#define U8V(v)  ((uint8_t)(v)  & 0xFFU)
#define U16V(v) ((uint16_t)(v) & 0xFFFFU)
#define U32V(v) ((uint32_t)(v) & 0xFFFFFFFFUL)

#define ROTL8(v, n) \
  (U8V((v) << (n)) | ((v) >> (8 - (n))))

#define ROTL16(v, n) \
  (U16V((v) << (n)) | ((v) >> (16 - (n))))

#define ROTL32(v, n) \
  (U32V((v) << (n)) | ((v) >> (32 - (n))))

#define ROTR8(v, n) ROTL8(v, 8 - (n))
#define ROTR16(v, n) ROTL16(v, 16 - (n))
#define ROTR32(v, n) ROTL32(v, 32 - (n))

void belt_init(uint8_t *ks, const uint8_t *k, size_t klen);
void belt_encryptx(uint32_t *out, const uint32_t *in, const uint32_t *ks, int enc);
void belt_encrypt(uint8_t *out, const uint8_t *in, const uint8_t *ks);
void belt_decrypt(uint8_t *out, const uint8_t *in, const uint8_t *ks);

#endif
